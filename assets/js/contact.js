/*
 *  テキスト・テキストボックス・セレクトボックス色変更
 */
jQuery(function($){
    var Target = $('.is-empty-text1');
    $(Target).on('change', function(){
        if ($(Target).val() !== ""){
            $(this).removeClass('red');
            $(this).addClass('white');
        } else {
            $(this).removeClass('white');
            $(this).addClass('red');
        }
    });
});

